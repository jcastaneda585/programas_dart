void main() {
  double num1 = 100;
  double num2 = 50;

  double suma = num2 + num1;
  double resta = num1 - num2;
  double multi = num1 * num2;
  double divi = num2 / num1;
  int divie = num2 ~/ num1;
  double re = num2 % num1;

  print("Suma : $num2 + $num1 = $suma ");
  print(" Resta : $num1 - $num2 = $resta");
  print("  multiplicación : $num1 x $num2 = $multi");
  print(" divición: $num2 / $num1 = $divi");
  print("  divición entera: $num2 / $num1 = $divie ");
  print("  residuo : $num2 % $num1 = $re");
}
